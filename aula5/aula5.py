'''
+, -, *, /, //, **, %, ()
'''
print('Multiplicação * ', 10 * 10)
print('Adição + ', 10 + 10)
print('Subtração - ', 10 - 10)
print('Divisão / ', 10 / 10)
print('potencialização ** ', 10 ** 2)
print('divisão com resultado inteiro // ', 10 // 3)
print('resto da divisão % ', 10 % 3)
print('Prioridade de parenteses ()', (5 + 2) * 5)